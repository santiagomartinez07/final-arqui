variableA: 0b0 
Q: 0b10000001 ; Multiplicador
Q_1: 0b0
M: 0b11111101; Multiplicando
count: 0x8

Init_Loop:
MOV ACC, TAM
MOV DPTR, #TAM
MOV ACC, [DPTR]
INV ACC
MOV A, ACC
MOV ACC, #ITERATOR
ADD ACC, A
MOV A, ACC
MOV ACC, #ITERATOR
MOV DPTR, #ITERATOR
MOV ACC, [DPTR]
ADD ACC, A
JN CTE
JMP CICLO_BOOTH
END_LOOP

CICLO_BOOTH:
MOV ACC, #Q(LSB)
MOV DPTR, #Q(LSB)
MOV ACC, [DPTR]
MOV A, ACC
MOV ACC, #G
MOV DPTR, #G
MOV ACC, A
MOV [DPTR], ACC
MOV ACC, #Q-1
MOV DPTR, #Q-1
MOV ACC, [DPTR]
INV ACC
MOV A, ACC
MOV ACC, #ITERATOR
ADD ACC, A
MOV A, ACC
MOV ACC, #G
MOV DPTR, #G
MOV ACC, [DPTR]
ADD ACC, A
JZ CTE

DESPLAZAMIENTO:
MOV ACC, #Q(LSB)
MOV DPTR, #Q(LSB)
MOV ACC, [DPTR]
JZ SUMA
JMP RESTA

SUMA:
MOV ACC, #M
MOV DPTR, #M
MOV ACC, [DPTR]
ADD ACC, A
MOV A, ACC
MOV ACC, #A
MOV DPTR, #A
MOV ACC, A
MOV [DPTR], ACC
JMP DESPLAZAMIENTO

RESTA:
MOV ACC, #M
MOV DPTR, #M
MOV ACC, [DPTR]
INV ACC
ADD ACC, A
MOV A, ACC
MOV ACC, #A
MOV DPTR, #A
MOV ACC, A
MOV [DPTR], ACC
JMP DESPLAZAMIENTO
